namespace Cleverence.TestTask.Core.Task1;

/// <summary>
/// Определяет контракт сервиса, который выполняет сжатие и восстановление строкового представления.
/// </summary>
public interface IStringCompressionService
{
    /// <summary>
    /// Сжимает исходную строку по правилам группировки одинаковых подряд идущих символов.
    /// </summary>
    /// <param name="input">Исходная строка, содержащая допустимые символы исходного формата.</param>
    /// <returns>Сжатое строковое представление.</returns>
    /// <remarks>Метод должен сохранять однозначность последующей декомпрессии.</remarks>
    string Compress(string input);

    /// <summary>
    /// Восстанавливает исходную строку из сжатого строкового представления.
    /// </summary>
    /// <param name="input">Строка в сжатом формате.</param>
    /// <returns>Восстановленная исходная строка.</returns>
    /// <remarks>Метод должен отклонять строки, которые не соответствуют принятому формату сжатия.</remarks>
    string Decompress(string input);
}
