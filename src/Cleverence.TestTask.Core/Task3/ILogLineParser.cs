namespace Cleverence.TestTask.Core.Task3;

/// <summary>
/// Определяет контракт парсера, который пытается преобразовать строку лога в структурированную запись.
/// </summary>
public interface ILogLineParser
{
    /// <summary>
    /// Пытается разобрать строку лога в структурированную запись.
    /// </summary>
    /// <param name="line">Исходная строка лога.</param>
    /// <param name="record">Результирующая запись при успешном разборе.</param>
    /// <returns>Истина, если строка соответствует поддерживаемому формату.</returns>
    /// <remarks>Метод не должен использовать исключения как штатный путь обработки невалидной строки.</remarks>
    bool TryParse(string line, out LogRecord? record);
}
